// BerkeOS — font.rs
// Built-in 8×16 monochrome bitmap font.
// Each glyph: 16 bytes, 1 byte per row, MSB = leftmost pixel.

pub const GLYPH_W: usize = 8;
pub const GLYPH_H: usize = 16;

pub fn get_glyph(ch: char) -> (&'static [u8], usize, usize) {
    let g: &[u8] = match ch {
        ' ' => &G_SPACE,
        '!' => &G_EXCL,
        '"' => &G_DQUOTE,
        '#' => &G_HASH,
        '$' => &G_DOLLAR,
        '%' => &G_PERCENT,
        '&' => &G_AMP,
        '\'' => &G_SQUOTE,
        '(' => &G_LPAREN,
        ')' => &G_RPAREN,
        '*' => &G_STAR,
        '+' => &G_PLUS,
        ',' => &G_COMMA,
        '-' => &G_MINUS,
        '.' => &G_DOT,
        '/' => &G_SLASH,
        '0' => &G_0,
        '1' => &G_1,
        '2' => &G_2,
        '3' => &G_3,
        '4' => &G_4,
        '5' => &G_5,
        '6' => &G_6,
        '7' => &G_7,
        '8' => &G_8,
        '9' => &G_9,
        ':' => &G_COLON,
        ';' => &G_SEMI,
        '<' => &G_LT,
        '=' => &G_EQ,
        '>' => &G_GT,
        '?' => &G_QUEST,
        '@' => &G_AT,
        'A' => &G_CA,
        'B' => &G_CB,
        'C' => &G_CC,
        'D' => &G_CD,
        'E' => &G_CE,
        'F' => &G_CF,
        'G' => &G_CG,
        'H' => &G_CH,
        'I' => &G_CI,
        'J' => &G_CJ,
        'K' => &G_CK,
        'L' => &G_CL,
        'M' => &G_CM,
        'N' => &G_CN,
        'O' => &G_CO,
        'P' => &G_CP,
        'Q' => &G_CQ,
        'R' => &G_CR,
        'S' => &G_CS,
        'T' => &G_CT,
        'U' => &G_CU,
        'V' => &G_CV,
        'W' => &G_CW,
        'X' => &G_CX,
        'Y' => &G_CY,
        'Z' => &G_CZ,
        '[' => &G_LBKT,
        '\\' => &G_BKSL,
        ']' => &G_RBKT,
        '^' => &G_CARET,
        '_' => &G_UNDER,
        '`' => &G_GRAVE,
        'a' => &G_LA,
        'b' => &G_LB,
        'c' => &G_LC,
        'd' => &G_LD,
        'e' => &G_LE,
        'f' => &G_LF,
        'g' => &G_LG,
        'h' => &G_LH,
        'i' => &G_LI,
        'j' => &G_LJ,
        'k' => &G_LK,
        'l' => &G_LL,
        'm' => &G_LM,
        'n' => &G_LN,
        'o' => &G_LO,
        'p' => &G_LP,
        'q' => &G_LQ,
        'r' => &G_LR,
        's' => &G_LS,
        't' => &G_LT2,
        'u' => &G_LU,
        'v' => &G_LV,
        'w' => &G_LW,
        'x' => &G_LX,
        'y' => &G_LY,
        'z' => &G_LZ,
        '{' => &G_LBRC,
        '|' => &G_PIPE,
        '}' => &G_RBRC,
        '~' => &G_TILDE,
        // Box drawing single
        '\u{2500}' => &B_H,
        '\u{2502}' => &B_V,
        '\u{250C}' => &B_TL,
        '\u{2510}' => &B_TR,
        '\u{2514}' => &B_BL,
        '\u{2518}' => &B_BR,
        '\u{251C}' => &B_VR,
        '\u{2524}' => &B_VL,
        '\u{252C}' => &B_HB,
        '\u{2534}' => &B_HT,
        '\u{253C}' => &B_X,
        // Box drawing double
        '\u{2550}' => &B_DH,
        '\u{2551}' => &B_DV,
        '\u{2554}' => &B_DTL,
        '\u{2557}' => &B_DTR,
        '\u{255A}' => &B_DBL,
        '\u{255D}' => &B_DBR,
        '\u{2560}' => &B_DVR,
        '\u{2563}' => &B_DVL,
        '\u{2566}' => &B_DHB,
        '\u{2569}' => &B_DHT,
        '\u{256C}' => &B_DX,
        // Block elements
        '\u{2588}' => &BLK_FULL,
        '\u{2580}' => &BLK_TOP,
        '\u{2584}' => &BLK_BOT,
        '\u{258C}' => &BLK_L,
        '\u{2590}' => &BLK_R,
        _ => &G_QUEST,
    };
    (g, GLYPH_W, GLYPH_H)
}

